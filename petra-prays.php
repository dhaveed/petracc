                                    <h3 class="title-box mt-10">A Watch Unto Mercy, Revival & Passover.</h3>
                                    <p>
                                        The Church's greatest tool in crisis and in peace remains the same- Prayer. We are admonished to pray in times of peace, how much more in times of uncertainty like we have today.
                                        <br>
                                        <br> Please note that there’s a big picture behind all that’s playing out in the world today- it is setting the stage for the revelation of the man of sin- the Antichrist and more importantly the rapture of the Church.
                                        <br>
                                        <br> The Church must not be caught unawares. We must pray through the purposes of God in these times. <strong><em>The womb of the intercessor is the clock of the World.</em></strong>

                                    </p>

                                    <p>There are three things we must make supplications for in this season:</p>

                                    <h5 class="title-caption" style="margin-bottom: 10px">1. MERCY </h5>
                                    <p>
                                        <span style="margin-bottom: 5px;">12am / 9am </span>
                                        <br> (Psalm 147:11, Psalm 85:7-8, Psalm 44:26, Psalm 79:11)
                                    </p>
                                    <p>
                                        This isn’t in respect to sin or judgement nor is it because of sin- consciousness but petitioning God for His swift, compassionate and powerful intervention in the affairs of the world. We ask that Jesus will heal our world of the pain, sorrow and hurts restoring beauty and order to a broken world.

                                    </p>
                                    <blockquote class="wp-block-quote">
                                        <p>
                                            "Father we come to you today, hoping in your Mercies.
                                            <br> We ask that you arise today and help our world. Bring us salvation, redemption and deliverance. Dear Father in your mercies hear the sighings and the groaning of this broken world that’s riddled with pain. Let healing and restoration flow through the world. Speak peace. Speak prosperity. Speak bliss. Speak once again over the Earth.
                                            <br> We speak peace to the nations. We speak healing. We speak light. Let joy and life be restored to the nations of the Earth."
                                        </p>
                                    </blockquote>

                                    <h5 class="title-caption" style="margin-bottom: 10px">2. REVIVAL </h5>
                                    <p>
                                        <span style="margin-bottom: 5px;">3am / 3pm </span>
                                        <br> (Psalm 22:27-28, Psalm 43:3, James 5:7)
                                    </p>
                                    <p>
                                        According to the scriptures, a mighty move of God is imminent when the darkness is greatest. The coming of the Lord is nigh as such we must pray in the last mighty move of the Spirit that brings in the greatest harvest of souls ever known to the history of mankind. This is the time for that last great move. I am fully persuaded that entire nations can be turned to the Lord in this season.

                                    </p>
                                    <blockquote class="wp-block-quote">
                                        <p>
                                            "Father in the name of Jesus, we declare that light and truth goes out into all the world leading the nations into your house.
                                            <br>Nations that heretofore were closed to the Gospel are now open.
                                            <br>We decree that all the nations and the ends of the earth remember and return to the Lord. Truly Jesus is the governor among the nations. Indeed we declare that the rain of your Spirit is falling in the Earth today and we reap the precious fruit of the earth."

                                        </p>
                                    </blockquote>

                                    <h5 class="title-caption" style="margin-bottom: 10px">2. PASSOVER </h5>
                                    <p>
                                        <span style="margin-bottom: 5px;">6am / 6pm </span>
                                        <br> (Exodus 12:13,21-24, 1 Cor 5:7)
                                    </p>
                                    <p>
                                        When death visited Egypt, Gods people were spared. Not only were they spared, the stage was set for Gods people to walk in the fulness of every promise that had gone ahead of them- complete liberty and wealth.
                                        <br>The days ahead will be marked by tremendous prosperity, innovation and light in the church. We must pray to position ourselves aright for it.

                                    </p>
                                    <blockquote class="wp-block-quote">
                                        <p>
                                            "Father I thank you for Christ, my Passover has been sacrificed for me. In the very same way, the angel of death passed over the house of the Israelites, I declare that everyone connected to me, to Petra and the household of Faith the world over are spared from the destroyer.
                                            <br> We declare that just as there was light in Goshen, there’s light in the church. We receive innovation, ideas, strategies of the Spirit that puts us 10 years ahead of the curve. We come out of this season with massive wealth, influence and authority. The balance of power and scale of wealth has tilted in my favour."

                                        </p>
                                    </blockquote>

